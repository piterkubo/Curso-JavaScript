// for numeros pares ou impares


for(let i = 0; i <=50; i++){
    if(i%2 == 0){
        console.log(`${i} é par`);
    }
    else{
        console.log(`${i} é impar`)
    }
}

let i = 0;

while(i <=50){
    if(i%2 == 0){
        console.log(`${i} é par`);
    }
    else{
        console.log(`${i} é impar`)
    }

    i++
}