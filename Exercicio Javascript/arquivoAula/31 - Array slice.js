// Slice retorna uma array a partir de outro array

// OBS: O array retornando é determinado pelo parametro que são os indices de inicio e fim do novo array


let numero = [0,1,2,3,4,5];

console.log(numero.slice(2,3)); // vai dar 2 pois vai iniciar em 2  até ele mesmo

console.log(numero.slice(3)); // vai dar 3,4,5, pois ele vai iniciar do 3 até o final do array